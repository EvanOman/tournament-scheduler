"""Deterministic spec comparison for eval scoring (DESIGN.md sec 5).

`score_spec` compares a materialized `TournamentSpec` (the agent's final draft)
against a `golden_spec.yaml` for the same brief, per constraint category. It is
pure and synchronous -- no LLM calls, no network, no randomness -- so it is
safe to run in CI and to self-check against `golden == golden` as a corpus
format gate.

Matching strategy
------------------
Divisions are matched by id (the agent is expected to pick the same short id
the persona's facts imply, e.g. "u10b" for "U10 Boys" -- ids are treated as a
stable identifier here, same as DESIGN.md sec 5 prescribes). Fields fall back
to name matching when ids differ. Teams, coaching conflicts, and team
avoidances are resolved through team *names* rather than raw ids, because
`SpecSession.add_teams` auto-derives team ids from names
(`tourneydesk/session.py::_derive_team_id`) and there is no guarantee the
agent's derived id matches the golden fixture's id even when the team is
otherwise correct. Time/field preferences resolve their `target` the same way
(division ids are matched directly; team targets are resolved via name), and
field ids inside a field preference are resolved to field names before
comparing.

Every category yields `matched` (count), `missing` (golden items absent or
wrong in the final spec -- recall misses), and `extra` (final items with no
golden counterpart -- precision misses / hallucination candidates), each with
a human-readable description. `hallucinated_count` sums `extra` across the
four *constraint* categories (coaching_conflicts, team_avoidances,
time_preferences, field_preferences) per DESIGN.md sec 5's hallucination
metric -- it excludes divisions/teams/fields because a wrong division param or
an extra team is a correctness bug, not a fabricated constraint.
"""

from __future__ import annotations

import re

from pydantic import BaseModel, Field

from tournament_scheduler.models import (
    CoachingConflict,
    DivisionSpec,
    FieldPreference,
    FieldSpec,
    TeamAvoidance,
    TeamSpec,
    TimePreference,
    TournamentSpec,
)

# Matches placeholder team names generated by `SpecSession.set_team_count`
# ("Team 1") and hand-authored golden fixtures using the same convention with
# a division prefix ("U10 Team 3"). Golden divisions where every team name
# matches this pattern are compared by count only, not by name.
_PLACEHOLDER_TEAM_NAME_RE = re.compile(r"^(?:\S+\s+)?team\s+\d+$", re.IGNORECASE)

_DIVISION_PARAM_FIELDS = (
    "field_size",
    "game_duration_minutes",
    "halftime_minutes",
    "buffer_minutes",
    "min_rest_minutes",
    "games_per_team",
    "pool_size",
    "bracket_after_pools",
)


# ---------------------------------------------------------------------------
# Result models
# ---------------------------------------------------------------------------


class CategoryScore(BaseModel):
    """Precision/recall/F1 plus explainable diffs for one constraint category."""

    category: str
    matched: int
    missing: list[str] = Field(default_factory=list)
    extra: list[str] = Field(default_factory=list)
    precision: float
    recall: float
    f1: float


class SpecScore(BaseModel):
    """Full per-category + overall comparison of a final spec against golden."""

    divisions: CategoryScore
    teams: CategoryScore
    fields: CategoryScore
    coaching_conflicts: CategoryScore
    team_avoidances: CategoryScore
    time_preferences: CategoryScore
    field_preferences: CategoryScore

    precision: float
    recall: float
    f1: float
    hallucinated_count: int

    def category_scores(self) -> list[CategoryScore]:
        return [
            self.divisions,
            self.teams,
            self.fields,
            self.coaching_conflicts,
            self.team_avoidances,
            self.time_preferences,
            self.field_preferences,
        ]


def _build_category(name: str, matched: int, missing: list[str], extra: list[str]) -> CategoryScore:
    precision_denom = matched + len(extra)
    recall_denom = matched + len(missing)
    precision = matched / precision_denom if precision_denom else 1.0
    recall = matched / recall_denom if recall_denom else 1.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0
    return CategoryScore(
        category=name,
        matched=matched,
        missing=missing,
        extra=extra,
        precision=precision,
        recall=recall,
        f1=f1,
    )


def _unwrap(value: object) -> object:
    """Unwrap an Enum to its plain value; pass everything else through."""
    return value.value if hasattr(value, "value") else value


# ---------------------------------------------------------------------------
# Divisions
# ---------------------------------------------------------------------------


def _division_param(division: DivisionSpec, name: str) -> object:
    return _unwrap(getattr(division, name))


def _division_match_map(final: TournamentSpec, golden: TournamentSpec) -> dict[str, str]:
    """Map golden division id -> matching final division id.

    Matches by identical id first, then by normalized name ("U9 Coed" matches
    whether the agent chose id 'u9', 'u9c', or 'u9_coed'). Ids are agent-derived
    implementation details, same as team ids -- name is the stable key.
    """
    final_by_id = {d.id: d for d in final.divisions}
    final_by_name = {d.name.strip().lower(): d for d in final.divisions}
    mapping: dict[str, str] = {}
    used: set[str] = set()
    for gd in golden.divisions:
        fd = final_by_id.get(gd.id)
        if fd is None or fd.id in used:
            fd = final_by_name.get(gd.name.strip().lower())
        if fd is not None and fd.id not in used:
            mapping[gd.id] = fd.id
            used.add(fd.id)
    return mapping


_BRACKET_KEYWORDS = ("bracket", "playoff", "elimination", "knockout", "knock-out", "finals")


def _compare_divisions(
    final: TournamentSpec,
    golden: TournamentSpec,
    facts_text: str | None = None,
) -> CategoryScore:
    """Compare divisions by id and scheduling params.

    `bracket_after_pools` is only compared when the brief facts actually
    mention a bracket/playoff: the flag is not yet solver-relevant (bracket
    phase lands at M7) and, when the facts are silent, the golden value is
    just the model default -- holding the agent to it would score an
    unstateable fact (same principle as facts-scoped team names).
    """
    facts_lower = facts_text.lower() if facts_text else None
    score_bracket = facts_lower is None or any(k in facts_lower for k in _BRACKET_KEYWORDS)
    param_fields = [name for name in _DIVISION_PARAM_FIELDS if score_bracket or name != "bracket_after_pools"]

    final_by_id = {d.id: d for d in final.divisions}
    div_map = _division_match_map(final, golden)
    mapped_final_ids = set(div_map.values())
    matched = 0
    missing: list[str] = []
    extra: list[str] = []

    for gd in golden.divisions:
        fd = final_by_id.get(div_map.get(gd.id, ""))
        if fd is None:
            missing.append(f"division '{gd.id}' ({gd.name}) is missing from the final spec.")
            continue
        diffs = [
            f"{name}: golden={_division_param(gd, name)!r} final={_division_param(fd, name)!r}"
            for name in param_fields
            if _division_param(gd, name) != _division_param(fd, name)
        ]
        if diffs:
            missing.append(f"division '{gd.id}' ({gd.name}) has mismatched params: {'; '.join(diffs)}.")
        else:
            matched += 1

    for fd in final.divisions:
        if fd.id not in mapped_final_ids:
            extra.append(f"division '{fd.id}' ({fd.name}) is not present in the golden spec.")

    return _build_category("divisions", matched, missing, extra)


# ---------------------------------------------------------------------------
# Teams
# ---------------------------------------------------------------------------


def _all_placeholder_names(teams: list[TeamSpec]) -> bool:
    return bool(teams) and all(_PLACEHOLDER_TEAM_NAME_RE.match(t.name.strip()) for t in teams)


def _compare_teams(
    final: TournamentSpec,
    golden: TournamentSpec,
    facts_text: str | None = None,
) -> CategoryScore:
    """Compare team rosters per division.

    Name-level matching only applies to golden teams whose names are actually
    *stateable* by the simulated director -- i.e. they appear in the brief's
    `facts` (when `facts_text` is given). A golden name absent from the facts
    can never be uttered by the persona, so holding the agent accountable for
    it would make the category structurally unscorable (13/15 corpus briefs
    have golden rosters that the facts never enumerate). Those teams -- and
    all-placeholder golden rosters -- degrade to count-only comparison.
    """
    facts_lower = facts_text.lower() if facts_text else None

    golden_by_div: dict[str, list[TeamSpec]] = {}
    for t in golden.teams:
        golden_by_div.setdefault(t.division_id, []).append(t)
    final_by_div: dict[str, list[TeamSpec]] = {}
    for t in final.teams:
        final_by_div.setdefault(t.division_id, []).append(t)
    div_map = _division_match_map(final, golden)

    matched = 0
    missing: list[str] = []
    extra: list[str] = []

    for division_id, gteams in golden_by_div.items():
        fteams = final_by_div.get(div_map.get(division_id, division_id), [])

        # Split golden teams into name-scoreable (persona could state the
        # name) and count-only (placeholder, or name untraceable to facts).
        if _all_placeholder_names(gteams):
            scoreable: list[TeamSpec] = []
        elif facts_lower is None:
            scoreable = [t for t in gteams if not _PLACEHOLDER_TEAM_NAME_RE.match(t.name.strip())]
        else:
            scoreable = [
                t
                for t in gteams
                if not _PLACEHOLDER_TEAM_NAME_RE.match(t.name.strip()) and t.name.strip().lower() in facts_lower
            ]

        gnames = {t.name.strip().lower() for t in scoreable}
        fnames = {t.name.strip().lower() for t in fteams}
        matched += len(gnames & fnames)
        for name in sorted(gnames - fnames):
            missing.append(f"division '{division_id}': team '{name}' is missing from the final spec.")

        # Teams beyond the name-scoreable set compare by count.
        count_only_golden = len(gteams) - len(gnames)
        count_only_final = len(fteams) - len(fnames & gnames)
        if count_only_golden or not gnames:
            if count_only_final == count_only_golden:
                if count_only_golden:
                    matched += 1
            elif count_only_final < count_only_golden:
                missing.append(
                    f"division '{division_id}': expected {count_only_golden} additional team(s) "
                    f"(names not enumerable from the brief facts -- count-only comparison), "
                    f"final spec has {count_only_final}."
                )
            else:
                extra.append(
                    f"division '{division_id}': final spec has {count_only_final - count_only_golden} "
                    f"more team(s) than the golden roster (count-only comparison)."
                )
        elif fnames - gnames:
            for name in sorted(fnames - gnames):
                extra.append(f"division '{division_id}': team '{name}' is not present in the golden spec.")

    return _build_category("teams", matched, missing, extra)


# ---------------------------------------------------------------------------
# Fields
# ---------------------------------------------------------------------------


def _window_pairs(f: FieldSpec) -> frozenset[tuple[str, str]]:
    return frozenset((w.start.isoformat(), w.end.isoformat()) for w in f.availability)


def _compare_fields(final: TournamentSpec, golden: TournamentSpec) -> CategoryScore:
    final_by_id = {f.id: f for f in final.fields}
    final_by_name = {f.name.strip().lower(): f for f in final.fields}
    matched_final_ids: set[str] = set()
    matched = 0
    missing: list[str] = []
    extra: list[str] = []

    for gf in golden.fields:
        ff = final_by_id.get(gf.id) or final_by_name.get(gf.name.strip().lower())
        if ff is None:
            missing.append(f"field '{gf.id}' ({gf.name}) is missing from the final spec.")
            continue
        matched_final_ids.add(ff.id)
        diffs: list[str] = []
        if _unwrap(gf.size) != _unwrap(ff.size):
            diffs.append(f"size: golden={_unwrap(gf.size)!r} final={_unwrap(ff.size)!r}")
        gwin, fwin = _window_pairs(gf), _window_pairs(ff)
        if gwin != fwin:
            diffs.append(f"availability: golden={sorted(gwin)} final={sorted(fwin)}")
        if diffs:
            missing.append(f"field '{gf.id}' ({gf.name}) has mismatches: {'; '.join(diffs)}.")
        else:
            matched += 1

    for ff in final.fields:
        if ff.id not in matched_final_ids:
            extra.append(f"field '{ff.id}' ({ff.name}) is not present in the golden spec.")

    return _build_category("fields", matched, missing, extra)


# ---------------------------------------------------------------------------
# Shared helpers: name resolution through each spec's own teams/fields
# ---------------------------------------------------------------------------


def _team_name_map(spec: TournamentSpec) -> dict[str, str]:
    return {t.id: t.name for t in spec.teams}


def _field_name_map(spec: TournamentSpec) -> dict[str, str]:
    return {f.id: f.name for f in spec.fields}


def _resolve_target(target: str, target_type: str, spec: TournamentSpec) -> str:
    """Resolve a preference's `target` to a stable comparison key.

    Division targets are compared by id directly (divisions are matched by id
    throughout this module). Team targets are resolved to the team's name,
    since a team's id is an implementation detail the agent derives itself.
    """
    if target_type == "division":
        div_names = {d.id: d.name for d in spec.divisions}
        return div_names.get(target, target).strip().lower()
    return _team_name_map(spec).get(target, target).strip().lower()


# ---------------------------------------------------------------------------
# Coaching conflicts
# ---------------------------------------------------------------------------


def _conflict_team_names(c: CoachingConflict, name_map: dict[str, str]) -> frozenset[str]:
    return frozenset(name_map.get(tid, tid).strip().lower() for tid in c.team_ids)


def _compare_coaching_conflicts(final: TournamentSpec, golden: TournamentSpec) -> CategoryScore:
    gnames_map = _team_name_map(golden)
    fnames_map = _team_name_map(final)
    final_by_coach = {c.coach_name.strip().lower(): c for c in final.coaching_conflicts}

    matched = 0
    missing: list[str] = []
    extra: list[str] = []
    matched_coach_keys: set[str] = set()

    for gc in golden.coaching_conflicts:
        key = gc.coach_name.strip().lower()
        fc = final_by_coach.get(key)
        gteams = _conflict_team_names(gc, gnames_map)
        if fc is None:
            missing.append(f"coaching conflict for '{gc.coach_name}' (teams: {sorted(gteams)}) is missing.")
            continue
        matched_coach_keys.add(key)
        fteams = _conflict_team_names(fc, fnames_map)
        if gteams == fteams:
            matched += 1
        else:
            missing.append(
                f"coaching conflict for '{gc.coach_name}': golden teams={sorted(gteams)} final teams={sorted(fteams)}."
            )

    for key, fc in final_by_coach.items():
        if key not in matched_coach_keys:
            extra.append(
                f"coaching conflict for '{fc.coach_name}' "
                f"(teams: {sorted(_conflict_team_names(fc, fnames_map))}) is not present in the golden spec."
            )

    return _build_category("coaching_conflicts", matched, missing, extra)


# ---------------------------------------------------------------------------
# Team avoidances
# ---------------------------------------------------------------------------


def _avoidance_team_names(a: TeamAvoidance, name_map: dict[str, str]) -> frozenset[str]:
    return frozenset(name_map.get(tid, tid).strip().lower() for tid in a.team_ids)


def _compare_team_avoidances(final: TournamentSpec, golden: TournamentSpec) -> CategoryScore:
    gnames_map = _team_name_map(golden)
    fnames_map = _team_name_map(final)
    final_by_key = {_avoidance_team_names(a, fnames_map): a for a in final.team_avoidances}

    matched = 0
    missing: list[str] = []
    extra: list[str] = []
    matched_keys: set[frozenset[str]] = set()

    for ga in golden.team_avoidances:
        key = _avoidance_team_names(ga, gnames_map)
        if key in final_by_key:
            matched += 1
            matched_keys.add(key)
        else:
            missing.append(f"team avoidance between {sorted(key)} is missing from the final spec.")

    for key in final_by_key:
        if key not in matched_keys:
            extra.append(f"team avoidance between {sorted(key)} is not present in the golden spec.")

    return _build_category("team_avoidances", matched, missing, extra)


# ---------------------------------------------------------------------------
# Time / field preferences
# ---------------------------------------------------------------------------


def _windows(pref: TimePreference) -> frozenset[tuple[str, str]]:
    return frozenset((w.start.isoformat(), w.end.isoformat()) for w in pref.preferred_windows)


def _compare_time_preferences(final: TournamentSpec, golden: TournamentSpec) -> CategoryScore:
    final_items = [
        (p, (_resolve_target(p.target, p.target_type, final), p.target_type)) for p in final.time_preferences
    ]
    matched_idx: set[int] = set()
    matched = 0
    missing: list[str] = []
    extra: list[str] = []

    for gp in golden.time_preferences:
        gkey = (_resolve_target(gp.target, gp.target_type, golden), gp.target_type)
        found = next((i for i, (_, fkey) in enumerate(final_items) if i not in matched_idx and fkey == gkey), None)
        if found is None:
            missing.append(f"time preference for {gp.target_type} '{gp.target}' is missing from the final spec.")
            continue
        matched_idx.add(found)
        fp, _ = final_items[found]
        if _windows(gp) == _windows(fp) and _unwrap(gp.priority) == _unwrap(fp.priority):
            matched += 1
        else:
            missing.append(
                f"time preference for {gp.target_type} '{gp.target}': "
                f"golden windows={sorted(_windows(gp))} priority={_unwrap(gp.priority)}; "
                f"final windows={sorted(_windows(fp))} priority={_unwrap(fp.priority)}."
            )

    for i, (fp, _) in enumerate(final_items):
        if i not in matched_idx:
            extra.append(f"time preference for {fp.target_type} '{fp.target}' is not present in the golden spec.")

    return _build_category("time_preferences", matched, missing, extra)


def _pref_field_names(pref: FieldPreference, name_map: dict[str, str]) -> frozenset[str]:
    return frozenset(name_map.get(fid, fid).strip().lower() for fid in pref.preferred_field_ids)


def _compare_field_preferences(final: TournamentSpec, golden: TournamentSpec) -> CategoryScore:
    gfield_names = _field_name_map(golden)
    ffield_names = _field_name_map(final)
    final_items = [
        (p, (_resolve_target(p.target, p.target_type, final), p.target_type)) for p in final.field_preferences
    ]
    matched_idx: set[int] = set()
    matched = 0
    missing: list[str] = []
    extra: list[str] = []

    for gp in golden.field_preferences:
        gkey = (_resolve_target(gp.target, gp.target_type, golden), gp.target_type)
        found = next((i for i, (_, fkey) in enumerate(final_items) if i not in matched_idx and fkey == gkey), None)
        if found is None:
            missing.append(f"field preference for {gp.target_type} '{gp.target}' is missing from the final spec.")
            continue
        matched_idx.add(found)
        fp, _ = final_items[found]
        gnames = _pref_field_names(gp, gfield_names)
        fnames = _pref_field_names(fp, ffield_names)
        if gnames == fnames and _unwrap(gp.priority) == _unwrap(fp.priority):
            matched += 1
        else:
            missing.append(
                f"field preference for {gp.target_type} '{gp.target}': "
                f"golden fields={sorted(gnames)} priority={_unwrap(gp.priority)}; "
                f"final fields={sorted(fnames)} priority={_unwrap(fp.priority)}."
            )

    for i, (fp, _) in enumerate(final_items):
        if i not in matched_idx:
            extra.append(f"field preference for {fp.target_type} '{fp.target}' is not present in the golden spec.")

    return _build_category("field_preferences", matched, missing, extra)


# ---------------------------------------------------------------------------
# Top-level
# ---------------------------------------------------------------------------

_HALLUCINATION_CATEGORIES = ("coaching_conflicts", "team_avoidances", "time_preferences", "field_preferences")


def score_spec(final: TournamentSpec, golden: TournamentSpec, facts_text: str | None = None) -> SpecScore:
    """Compare `final` (the agent's materialized spec) against `golden`.

    Pure function: no I/O, no LLM calls. Safe to call with `final is golden`
    (or two structurally-identical specs) as a corpus format self-check --
    that must always yield P=R=F1=1.0 and hallucinated_count=0.

    `facts_text` (the brief's ground-truth prose) scopes team-name matching to
    names the simulated director could actually state -- see `_compare_teams`.
    """
    divisions = _compare_divisions(final, golden, facts_text=facts_text)
    teams = _compare_teams(final, golden, facts_text=facts_text)
    fields = _compare_fields(final, golden)
    coaching_conflicts = _compare_coaching_conflicts(final, golden)
    team_avoidances = _compare_team_avoidances(final, golden)
    time_preferences = _compare_time_preferences(final, golden)
    field_preferences = _compare_field_preferences(final, golden)

    categories = [
        divisions,
        teams,
        fields,
        coaching_conflicts,
        team_avoidances,
        time_preferences,
        field_preferences,
    ]
    total_matched = sum(c.matched for c in categories)
    total_missing = sum(len(c.missing) for c in categories)
    total_extra = sum(len(c.extra) for c in categories)

    if total_matched == 0 and total_missing == 0 and total_extra == 0:
        precision = recall = f1 = 1.0
    else:
        precision = total_matched / (total_matched + total_extra) if (total_matched + total_extra) else 1.0
        recall = total_matched / (total_matched + total_missing) if (total_matched + total_missing) else 1.0
        f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0

    by_name = {c.category: c for c in categories}
    hallucinated_count = sum(len(by_name[name].extra) for name in _HALLUCINATION_CATEGORIES)

    return SpecScore(
        divisions=divisions,
        teams=teams,
        fields=fields,
        coaching_conflicts=coaching_conflicts,
        team_avoidances=team_avoidances,
        time_preferences=time_preferences,
        field_preferences=field_preferences,
        precision=precision,
        recall=recall,
        f1=f1,
        hallucinated_count=hallucinated_count,
    )
